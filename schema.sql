  ('Breakfix'), ('Devlopment Team'), ('Integration');

CREATE TABLE IF NOT EXISTS resources (
  id   TINYINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(60) NOT NULL UNIQUE
);

